// 12. các phép so sánh

let a = 3;
let b = 3;
let c = "3";

console.log(typeof b);
console.log(typeof c);

// các phép so sánh thông thường toán học
console.log(a > b);
console.log(a < b);
console.log(a <= b);
console.log(a >= b);
console.log(a != b);
console.log(c != b); // lưu ý

// so sánh bằng == (k quan tâm đến kiểu của biến)
console.log(a == b);
console.log(c == b);

// so sánh tuyệt đối (===) so sánh gtri và kiểu dl
console.log(a === b);
console.log(c === b);

console.log("Phần 2");
console.log(a != b);
console.log(c != b); // k qt kiểu của biến

console.log(a !== b);
console.log(c !== b); // So sánh gtri và kiểu dữ liệu
